





class _DebugValveToFile(object):

	################################################################################################################################
	## Constructor
	################################################################################################################################

	#
	# Constructor method.
	#
	def __init__(self, filePath:str):
		assert isinstance(filePath, str)
		self.filePath = filePath
	#

	################################################################################################################################
	## Public Properties
	################################################################################################################################

	################################################################################################################################
	## Helper Methods
	################################################################################################################################

	################################################################################################################################
	## Public Methods
	################################################################################################################################

	def print(self, *args):
		s = " ".join(*args) + "\n"
		with open(self.filePath, "a") as f:
			f.write(s)
	#

	def __call__(self, *args):
		s = " ".join(*args) + "\n"
		with open(self.filePath, "a") as f:
			f.write(s)
	#

